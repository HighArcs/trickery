import java.util.Scanner;

class U1_L4_Activity_One {
    public static void main(String[] args) {

        Scanner s = new Scanner(System.in);

        int i = s.nextInt();
        s.close();

        i++;
        System.out.println("number is now " + i);
        i++;
        System.out.println("number is now " + i);
        i++;
        System.out.println("number is now " + i);
        i++;
        System.out.println("number is now " + i);
        i--;
        System.out.println("number is now " + i);
        i--;
        System.out.println("number is now " + i);
        i--;
        System.out.println("number is now " + i);
        i--;
        System.out.println("number is now " + i);
    }
}